i = int(input('Digite um número: '))
print('O dobro de {} é {}'.format(i, (i*2)))
print('O triplo de {} é {}'.format(i, (i*3)))
print('O quadrado de {} é {}'.format(i, i**2))
print('A raíz quadrada de {} é {}'.format(i, i**0.5))

print('O dobro de {} é {} \nO triplo de {} é {} \nO quadrado de {} é {} \nA raíz quadrada de {} é {}'.format(i, i*2, i, i*3, i, i**2, i, i**0.5))